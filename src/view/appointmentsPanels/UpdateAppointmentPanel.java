    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (selectedOldStudent == null || selectedOldDate == null || selectedOldTime == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a row to update.");
        return;
        }

        String newStudent = txtStudentName.getText();
        String newCounselor = cbCounselor.getSelectedItem().toString();
        String newTime = cbTime.getSelectedItem().toString();
        String newStatus = cbStatus.getSelectedItem().toString();
        String newDate = new java.text.SimpleDateFormat("yyyy-MM-dd").format(dcDate.getDate());

        boolean success = controllers.AppointmentsController.updateAppointment(
            selectedOldStudent, selectedOldDate, selectedOldTime, // old
            newStudent, newCounselor, newDate, newTime, newStatus // new
        );

        if (success) {
            javax.swing.JOptionPane.showMessageDialog(this, "Appointment updated.");
            loadAppointmentData();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Update failed.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

