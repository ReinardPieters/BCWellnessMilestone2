    private void populateCounselorComboBox() {
        cbCounselor.removeAllItems(); // Clear existing items if any

        ArrayList<String> counselorNames = CounselorController.getAllCounselorNames();
        for (String name : counselorNames) {
            cbCounselor.addItem(name);
        }
    }
    private void initializeStatusComboBox() {
        cbStatus.removeAllItems();  // Clear any existing items
        cbStatus.addItem("Completed");
        cbStatus.addItem("Not Completed");
    }
    private void setupTableSelectionListener() {
    jTable1.getSelectionModel().addListSelectionListener(event -> {
        if (!event.getValueIsAdjusting() && jTable1.getSelectedRow() != -1) {
            int row = jTable1.getSelectedRow();

            Object studentObj = jTable1.getValueAt(row, 0);
            Object counselorObj = jTable1.getValueAt(row, 1);
            Object dateObj = jTable1.getValueAt(row, 2);
            Object timeObj = jTable1.getValueAt(row, 3);
            Object statusObj = jTable1.getValueAt(row, 4);

            if (studentObj == null || counselorObj == null || dateObj == null || timeObj == null || statusObj == null) {
                System.out.println("Row contains null values. Skipping.");
                return;
            }

            String student = studentObj.toString();
            String counselor = counselorObj.toString();
            String dateStr = dateObj.toString();   // Format: yyyy-MM-dd
            String time = timeObj.toString();
            String status = statusObj.toString();

            // Store old values for updating later
            selectedOldStudent = student;
            selectedOldDate = dateStr;
            selectedOldTime = time;

            // Set values in UI components
            txtStudentName.setText(student);
            cbCounselor.setSelectedItem(counselor);
            cbTime.setSelectedItem(time);
            cbStatus.setSelectedItem(status);

            try {
                java.util.Date parsedDate = new java.text.SimpleDateFormat("yyyy-MM-dd").parse(dateStr);
                dcDate.setDate(parsedDate);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    });
}



    private void loadAppointmentData() {
        String[] columnNames = {"Student", "Counselor", "Date", "Time", "Status"};

        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(columnNames, 0);
        jTable1.setModel(model);  // Replace existing dummy model

        java.util.List<String[]> appointments = getAllAppointments();

        for (String[] row : appointments) {
            model.addRow(row);
        }
    }
    
